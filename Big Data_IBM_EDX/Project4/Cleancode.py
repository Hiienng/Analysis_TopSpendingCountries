"""
This is a sample Python module that defines a function to add two numbers.
"""

def add(number_one, number_two):
    """
    This function returns the sum of two numbers.
    """
    return number_one + number_two

FST_NUM = 4
SEC_NUM = 5
TOTAL = add(FST_NUM, SEC_NUM)
print(f"The sum of {FST_NUM} and {SEC_NUM} is {TOTAL}")
